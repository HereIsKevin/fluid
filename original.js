function original_diff(oldValues, newValues) {
  const points = [];

  for (let y = 0; y < oldValues.length; y++) {
    for (let x = 0; x < newValues.length; x++) {
      if (newValues[y] === oldValues[x]) {
        points.push([y, x]);
      }
    }
  }

  const keep = [];
  let last = [-1, -1];

  for (const point of points) {
    if (point[0] > last[0] && point[1] > last[1]) {
      last = point;
      keep.push(last);
    }
  }

  return keep;
}

